package com.dangdang.readerV5.reponse;

public class SearchBarList {
	String barArticles;
	String barId;
	String barMembers;
	String barOwner;
	String barPic;
	String description;
	String score;
	String title;
	public String getBarArticles() {
		return barArticles;
	}
	public void setBarArticles(String barArticles) {
		this.barArticles = barArticles;
	}
	public String getBarId() {
		return barId;
	}
	public void setBarId(String barId) {
		this.barId = barId;
	}
	public String getBarMembers() {
		return barMembers;
	}
	public void setBarMembers(String barMembers) {
		this.barMembers = barMembers;
	}
	public String getBarOwner() {
		return barOwner;
	}
	public void setBarOwner(String barOwner) {
		this.barOwner = barOwner;
	}
	public String getBarPic() {
		return barPic;
	}
	public void setBarPic(String barPic) {
		this.barPic = barPic;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
}
