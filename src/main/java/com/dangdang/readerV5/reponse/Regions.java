package com.dangdang.readerV5.reponse;

public class Regions {
	String id;
	String is_cod;
	String is_cod_pos;
	String is_pick_up;
	String name;
	String zip_format;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIs_cod() {
		return is_cod;
	}
	public void setIs_cod(String is_cod) {
		this.is_cod = is_cod;
	}
	public String getIs_cod_pos() {
		return is_cod_pos;
	}
	public void setIs_cod_pos(String is_cod_pos) {
		this.is_cod_pos = is_cod_pos;
	}
	public String getIs_pick_up() {
		return is_pick_up;
	}
	public void setIs_pick_up(String is_pick_up) {
		this.is_pick_up = is_pick_up;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getZip_format() {
		return zip_format;
	}
	public void setZip_format(String zip_format) {
		this.zip_format = zip_format;
	}
	

}
