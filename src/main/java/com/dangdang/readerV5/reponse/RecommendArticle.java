package com.dangdang.readerV5.reponse;

public class RecommendArticle {
	String digestAuthor;
	String digestContent;
	String digestDescription;
	String digestId;
	String digestPic;
	String digestType;
	Integer flag;
	Integer hits;
	String sourceId;
	String sourceName;
	public String getDigestAuthor() {
		return digestAuthor;
	}
	public void setDigestAuthor(String digestAuthor) {
		this.digestAuthor = digestAuthor;
	}
	public String getDigestContent() {
		return digestContent;
	}
	public void setDigestContent(String digestContent) {
		this.digestContent = digestContent;
	}
	public String getDigestDescription() {
		return digestDescription;
	}
	public void setDigestDescription(String digestDescription) {
		this.digestDescription = digestDescription;
	}
	public String getDigestId() {
		return digestId;
	}
	public void setDigestId(String digestId) {
		this.digestId = digestId;
	}
	public String getDigestPic() {
		return digestPic;
	}
	public void setDigestPic(String digestPic) {
		this.digestPic = digestPic;
	}
	public String getDigestType() {
		return digestType;
	}
	public void setDigestType(String digestType) {
		this.digestType = digestType;
	}
	public Integer getFlag() {
		return flag;
	}
	public void setFlag(Integer flag) {
		this.flag = flag;
	}
	public Integer getHits() {
		return hits;
	}
	public void setHits(Integer hits) {
		this.hits = hits;
	}
	public String getSourceId() {
		return sourceId;
	}
	public void setSourceId(String sourceId) {
		this.sourceId = sourceId;
	}
	public String getSourceName() {
		return sourceName;
	}
	public void setSourceName(String sourceName) {
		this.sourceName = sourceName;
	}
	

}
